namespace Witchcraft;

trait Hydrate
{
    public function hydrate($data = [])
    {
        foreach ($data as $key => $value) {
            $method = "set" . ucfirst($key);
            if (method_exists($this, $method)) {
                /* Try to use setter */
                call_user_func([$this, $method], $value);
            } else {
                /* Or fallback to setting property directly */
                $this->$key = $value;
            }
        }
    }
}
